package service;

public class MemberScore {
		private int member_score_no;
		private int member_no;
		private int score;
		
		public int getMember_score_no() {
			return member_score_no;
		}
		public void setMember_score_no(int member_score_no) {
			this.member_score_no = member_score_no;
		}
		public int getMember_no() {
			return member_no;
		}
		public void setMember_no(int member_no) {
			this.member_no = member_no;
		}
		public int getScore() {
			return score;
		}
		public void setScore(int score) {
			this.score = score;
		}
		
		
}
