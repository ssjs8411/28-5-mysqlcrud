package service;

public class StudentAddr {

	public int student_addr_no;
	public int student_no;
	public String student_addr_content;
	
	public int getStudent_addr_no() {
		return student_addr_no;
	}
	public void setStudent_addr_no(int student_addr_no) {
		this.student_addr_no = student_addr_no;
	}
	public int getStudent_no() {
		return student_no;
	}
	public void setStudent_no(int student_no) {
		this.student_no = student_no;
	}
	public String getStudent_addr_content() {
		return student_addr_content;
	}
	public void setStudent_addr_content(String student_addr_content) {
		this.student_addr_content = student_addr_content;
	}
}
